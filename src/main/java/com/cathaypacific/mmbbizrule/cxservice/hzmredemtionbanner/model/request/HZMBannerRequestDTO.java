package com.cathaypacific.mmbbizrule.cxservice.hzmredemtionbanner.model.request;

public class HZMBannerRequestDTO{

	private String origin;
	
	private String destination;
	
	private String rbd;
	
	private String iataNum;
	
	private String creationDate;
	
	private String carrierCode;
	
	private String flightNum;
	
	private String departureDate;

	public String getOrigin() {
		return origin;
	}

	public void setOrigin(String origin) {
		this.origin = origin;
	}

	public String getDestination() {
		return destination;
	}

	public void setDestination(String destination) {
		this.destination = destination;
	}

	public String getRbd() {
		return rbd;
	}

	public void setRbd(String rbd) {
		this.rbd = rbd;
	}

	public String getIataNum() {
		return iataNum;
	}

	public void setIataNum(String iataNum) {
		this.iataNum = iataNum;
	}

	public String getCreationDate() {
		return creationDate;
	}

	public void setCreationDate(String creationDate) {
		this.creationDate = creationDate;
	}

	public String getCarrierCode() {
		return carrierCode;
	}

	public void setCarrierCode(String carrierCode) {
		this.carrierCode = carrierCode;
	}

	public String getFlightNum() {
		return flightNum;
	}

	public void setFlightNum(String flightNum) {
		this.flightNum = flightNum;
	}

	public String getDepartureDate() {
		return departureDate;
	}

	public void setDepartureDate(String departureDate) {
		this.departureDate = departureDate;
	}
	
	
	
}
